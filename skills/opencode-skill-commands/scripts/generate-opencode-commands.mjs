#!/usr/bin/env node
import fs from "node:fs";
import os from "node:os";
import path from "node:path";

const argv = process.argv.slice(2);
const args = new Set(argv);

const overwrite = args.has("--overwrite");
const dryRun = args.has("--dry-run");
const project = args.has("--project");

function optionValue(name) {
  const prefix = `${name}=`;
  const inline = argv.find((arg) => arg.startsWith(prefix));
  if (inline) return inline.slice(prefix.length);

  const index = argv.indexOf(name);
  if (index >= 0 && argv[index + 1] && !argv[index + 1].startsWith("--")) {
    return argv[index + 1];
  }

  return undefined;
}

function expandHome(input) {
  if (!input) return input;
  if (input === "~") return os.homedir();
  if (input.startsWith("~/")) return path.join(os.homedir(), input.slice(2));
  return input;
}

const home = os.homedir();
const cwd = process.cwd();

const explicitSkillDirs = argv
  .flatMap((arg, index) => {
    if (arg === "--skills-dir" && argv[index + 1]) return [argv[index + 1]];
    if (arg.startsWith("--skills-dir=")) return [arg.slice("--skills-dir=".length)];
    return [];
  })
  .map((dir) => path.resolve(expandHome(dir)));

const defaultSkillRoots = [
  path.join(home, ".agents/skills"),
  path.join(home, ".claude/skills"),
  path.join(home, ".config/opencode/skills"),
  path.join(home, ".config/opencode/skill"),
  path.join(cwd, ".opencode/skills"),
  path.join(cwd, ".opencode/skill"),
  path.join(cwd, ".claude/skills"),
  path.join(cwd, ".agents/skills"),
];

const skillRoots = explicitSkillDirs.length > 0 ? explicitSkillDirs : defaultSkillRoots;

const targetDirOption = optionValue("--target-dir");
const targetDir = targetDirOption
  ? path.resolve(expandHome(targetDirOption))
  : project
    ? path.join(cwd, ".opencode/commands")
    : path.join(home, ".config/opencode/commands");

const builtIns = new Set([
  "help",
  "init",
  "undo",
  "redo",
  "share",
  "compact",
  "summarize",
  "editor",
  "connect",
  "details",
]);

function readFrontmatter(file) {
  const text = fs.readFileSync(file, "utf8");
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return {};

  const frontmatter = {};
  let currentKey = null;
  let currentBlock = [];

  function flushBlock() {
    if (currentKey) {
      frontmatter[currentKey] = currentBlock.join("\n").trim();
      currentKey = null;
      currentBlock = [];
    }
  }

  for (const rawLine of match[1].split(/\r?\n/)) {
    const block = rawLine.match(/^([A-Za-z0-9_-]+):\s*\|-\s*$/);
    if (block) {
      flushBlock();
      currentKey = block[1];
      continue;
    }

    if (currentKey) {
      if (/^\s+/.test(rawLine) || rawLine.trim() === "") {
        currentBlock.push(rawLine.replace(/^\s{2}/, ""));
        continue;
      }
      flushBlock();
    }

    const pair = rawLine.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (pair) {
      frontmatter[pair[1]] = pair[2].replace(/^["']|["']$/g, "");
    }
  }

  flushBlock();
  return frontmatter;
}

function isValidSkillName(name) {
  return /^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(name);
}

function findSkills() {
  const found = new Map();
  const duplicates = [];

  for (const root of skillRoots) {
    if (!fs.existsSync(root)) continue;

    for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
      const skillDir = path.join(root, entry.name);
      if (!entry.isDirectory()) {
        if (!entry.isSymbolicLink()) continue;

        try {
          if (!fs.statSync(skillDir).isDirectory()) continue;
        } catch {
          continue;
        }
      }

      const skillFile = path.join(skillDir, "SKILL.md");
      if (!fs.existsSync(skillFile)) continue;

      const meta = readFrontmatter(skillFile);
      const name = meta.name || entry.name;
      const description = meta.description || `Use the ${name} skill directly`;

      if (!isValidSkillName(name)) continue;

      const skill = { name, description, skillFile, root };
      if (found.has(name)) {
        duplicates.push({ name, kept: found.get(name).skillFile, skipped: skillFile });
        continue;
      }

      found.set(name, skill);
    }
  }

  return {
    skills: [...found.values()].sort((a, b) => a.name.localeCompare(b.name)),
    duplicates,
  };
}

function oneLine(value, max = 120) {
  const normalized = value.replace(/\s+/g, " ").trim();
  return normalized.length > max ? `${normalized.slice(0, max - 3)}...` : normalized;
}

function commandBody(skill) {
  const shortDescription = oneLine(skill.description);

  return `---
description: Use the ${skill.name} skill: ${shortDescription}
---

Apply the \`${skill.name}\` skill.

$ARGUMENTS
`;
}

function isGeneratedCommand(file) {
  if (!fs.existsSync(file)) return false;
  const text = fs.readFileSync(file, "utf8");
  if (text.includes("Generated by opencode-skill-commands.")) return true;

  const fm = /^---\r?\ndescription: Use the [a-z0-9]+(?:-[a-z0-9]+)* skill: /m.test(text);
  if (!fm) return false;

  return (
    text.includes("$ARGUMENTS") &&
    (
      text.includes("Apply the `") ||
      text.includes("Use the native OpenCode skill tool")
    )
  );
}

const { skills, duplicates } = findSkills();
const stats = {
  created: [],
  updated: [],
  deletedStale: [],
  skippedExisting: [],
  skippedConflicts: [],
};

if (!dryRun) fs.mkdirSync(targetDir, { recursive: true });

function currentCommandNames() {
  return new Set(skills.filter((skill) => !builtIns.has(skill.name)).map((skill) => skill.name));
}

function pruneStaleGeneratedCommands() {
  if (!fs.existsSync(targetDir)) return;

  const current = currentCommandNames();
  for (const entry of fs.readdirSync(targetDir, { withFileTypes: true })) {
    if (!entry.isFile() || !entry.name.endsWith(".md")) continue;

    const commandName = path.basename(entry.name, ".md");
    if (current.has(commandName)) continue;

    const commandFile = path.join(targetDir, entry.name);
    if (!isGeneratedCommand(commandFile)) continue;

    stats.deletedStale.push(commandName);
    if (!dryRun) fs.unlinkSync(commandFile);
  }
}

pruneStaleGeneratedCommands();

for (const skill of skills) {
  if (builtIns.has(skill.name)) {
    stats.skippedConflicts.push(skill.name);
    continue;
  }

  const commandFile = path.join(targetDir, `${skill.name}.md`);
  const exists = fs.existsSync(commandFile);

  if (exists && !overwrite) {
    stats.skippedExisting.push(skill.name);
    continue;
  }

  if (exists && !isGeneratedCommand(commandFile) && overwrite) {
    stats.skippedExisting.push(skill.name);
    continue;
  }

  if (exists) stats.updated.push(skill.name);
  else stats.created.push(skill.name);

  if (!dryRun) {
    fs.writeFileSync(commandFile, commandBody(skill), "utf8");
  }
}

console.log(`Target: ${targetDir}`);
console.log(`Discovered skills: ${skills.length}`);
console.log(`Created: ${stats.created.length}`);
console.log(`Updated: ${stats.updated.length}`);
console.log(`Deleted stale: ${stats.deletedStale.length}`);
console.log(`Skipped existing: ${stats.skippedExisting.length}`);
console.log(`Skipped conflicts: ${stats.skippedConflicts.length}`);
console.log(`Duplicate skill names: ${duplicates.length}`);

const changed = [...stats.created, ...stats.updated].sort();
if (changed.length) {
  console.log("\nCommands:");
  for (const name of changed) console.log(`/${name}`);
}

if (stats.deletedStale.length) {
  console.log("\nDeleted stale commands:");
  for (const name of stats.deletedStale.sort()) console.log(`/${name}`);
}

if (stats.skippedConflicts.length) {
  console.log("\nConflicts:");
  for (const name of stats.skippedConflicts.sort()) console.log(`/${name}`);
}

if (duplicates.length) {
  console.log("\nDuplicates:");
  for (const duplicate of duplicates) {
    console.log(`${duplicate.name}: kept ${duplicate.kept}; skipped ${duplicate.skipped}`);
  }
}
